  `status` enum('active','inactive') COLLATE utf8_unicode_ci NOT NULL DEFAULT 'active',
  `majority_leader` tinyint(1) NOT NULL DEFAULT '0',
  `majority_whip` tinyint(1) NOT NULL DEFAULT '0',
